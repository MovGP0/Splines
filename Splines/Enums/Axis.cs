namespace Splines.Enums;

/// <summary>
/// Represents the three primary axes in a 3D Cartesian coordinate system.
/// </summary>
[EnumExtensions]
public enum Axis
{
    /// <summary>
    /// The X axis, typically representing the horizontal direction in 3D space.
    /// </summary>
    X = 0,

    /// <summary>
    /// The Y axis, typically representing the vertical direction in 3D space.
    /// </summary>
    Y = 1,

    /// <summary>
    /// The Z axis, typically representing the depth direction in 3D space.
    /// </summary>
    Z = 2
}
